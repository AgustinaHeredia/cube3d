#include "libft.h"

int	pf_arg_c(t_sc *sc)
{
	char	ch;

	ch = va_arg(sc->arg, int);
	if (write(1, &ch, 1) == -1)
	{
		sc->error = -1;
		return (-1);
	}
	sc -> len += 1;
	return (1);
}
