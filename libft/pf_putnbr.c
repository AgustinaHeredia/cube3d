#include "libft.h"

int	pf_putnbr(int nb)
{
	char	a;

	if (nb == -2147483648)
		return (write(1, "-2147483648", 11));
	if (nb < 0)
	{
		nb = -nb;
		if (write(1, "-", 1) == -1)
			return (-1);
	}
	if (nb >= 10)
	{
		if (pf_putnbr(nb / 10) == -1)
			return (-1);
	}
	a = nb % 10 + '0';
	if (write(1, &a, 1) == -1)
		return (-1);
	return (1);
}
