#include "libft.h"

int	ft_isalnum(int c)
{
	int	result;

	result = 0;
	if ((c > 64 && c < 91) || (c > 96 && c < 123) || (c > 47 && c < 58))
	{
		result = 8;
	}
	return (result);
}
